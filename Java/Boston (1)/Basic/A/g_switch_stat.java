
public class g_switch_stat {
    public static void main(String[] args) 
    {
        int age;
        age=3;
        switch (age) 
        {
            case 1:
                System.out.println("You are in one");   
                break;
            case 2:
                System.out.println("You are in two");
                break;    
            case 3:
                System.out.println("You are in Three");   
                break;
            default:
                System.out.println("default");
                break;
        }
    }
}
